public class StringExercise {
    public static void main(String[] args) {
        String pinkFloydAnotherBrickInTheWall = "We don't need no education \nWe don't need no thought control";
        System.out.println(pinkFloydAnotherBrickInTheWall);

        String quotations = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quotations);

        String escapeSingleBackslash = "In windows, the main drive is usually C:\\";
        System.out.println(escapeSingleBackslash);

        String escapeMultipleBackslashes = "I can do backslashes\\, double backslashes \\\\,\nand the amazing triple backslash\\\\\\!";
        System.out.println(escapeMultipleBackslashes);
    }
}

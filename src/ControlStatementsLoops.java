public class ControlStatementsLoops {

    public static void main(String[] args) {
        int i = 5;
        while(i<=15) {
            System.out.print(i + " ");
            i++;
        }

        int a = 0;
        do {
            System.out.println(a);
            a+=2;
        } while(a<=100);
    }
}
